public class CharTest {
    public static void main(String[] args) {
        char c1 = 'a';
        System.out.println("c1 = " + c1);
        System.out.println("对应的编号是:" + (int)c1);

        char c2 = 98;
        System.out.println("c2 = " + c2);
        System.out.println("对应的编号是:" + (int)c2);
    }
}
