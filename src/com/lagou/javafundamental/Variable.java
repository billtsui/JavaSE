package com.lagou.javafundamental;

public class Variable {
    /**
     * 变量的声明方式
     * 
     * 数据类型 变量名 = 初始值;
     * 
     */
    public static void main(String[] args) {
        int age = 31;
        String name = "Bill Tsui";
        System.out.println("My name is " + name + " and  I'm " + age);
    }
}
